def test_multi_device_risk_analysis_api_runs_full_tool_chain_and_saves_history(
    monkeypatch,
) -> None:
    client, SessionLocal = _build_client()
    db = SessionLocal()
    try:
        _seed_devices(db)
    finally:
        db.close()

    knowledge_queries: list[str] = []

    def fake_search(input_data):
        knowledge_queries.append(input_data.query)
        if "E101" in input_data.query:
            return _knowledge_result(
                alarm_code="E101",
                source="e101_maintenance_manual.md#chunk-0",
                filename="e101_maintenance_manual.md",
                content="E101 indicates temperature abnormality and requires cooling inspection.",
                distance=0.18,
            )
        if "E201" in input_data.query:
            return _knowledge_result(
                alarm_code="E201",
                source="e201_vibration_manual.md#chunk-0",
                filename="e201_vibration_manual.md",
                content="E201 indicates abnormal vibration and requires bearing inspection.",
                distance=0.16,
            )
        if "E203" in input_data.query:
            return _knowledge_result(
                alarm_code="E203",
                source="e203_controller_manual.md#chunk-0",
                filename="e203_controller_manual.md",
                content="E203 indicates abnormal motor operation and requires load inspection.",
                distance=0.2,
            )
        if "E404" in input_data.query:
            return _knowledge_result(
                alarm_code="E404",
                source="e404_sensor_manual.md#chunk-0",
                filename="e404_sensor_manual.md",
                content="E404 indicates communication abnormality and requires link inspection.",
                distance=0.25,
            )
        return KnowledgeSearchToolResult(ok=True, results=[])

    monkeypatch.setattr(risk_analysis_module, "run_search_knowledge_tool", fake_search)
    provider = MockLLMProvider(
        response={
            "summary": "Fleet risk report generated by LLM.",
            "overall_risk_level": "high",
            "key_findings": ["DEV-001, DEV-002, and DEV-003 have unresolved alarms."],
            "recommended_actions": ["Prioritize alarm devices and inspect related subsystems."],
            "warnings": [],
        }
    )
    app.dependency_overrides[get_llm_provider] = lambda: provider

    try:
        response = client.post(
            "/agent/risk-analysis",
            json={"query": "Analyze all current device risks and check DEV-999", "knowledge_top_k": 3},
        )
        history_response = client.get("/diagnosis/history")

        assert response.status_code == 200
        data = response.json()
        assert data["summary"] == "Fleet risk report generated by LLM."
        assert data["overall_risk_level"] == "high"
        assert "Data quality warning" in " ".join(data["warnings"])
        assert "DEV-999" in " ".join(data["warnings"])
        assert "DEV-999" not in [
            item["device"]["device_code"] for item in data["device_risks"]
        ]
        assert data["tools_used"] == [
            "list_devices",
            "get_device_status",
            "get_device_alarms",
            "search_knowledge",
            "llm_risk_report",
        ]
        assert data["sources"] == [
            "e203_controller_manual.md#chunk-0",
            "e404_sensor_manual.md#chunk-0",
            "e101_maintenance_manual.md#chunk-0",
            "e201_vibration_manual.md#chunk-0",
        ]

        device_risks = {
            item["device"]["device_code"]: item for item in data["device_risks"]
        }
        assert list(device_risks)[0] == "DEV-003"
        assert device_risks["DEV-001"]["knowledge_sources"] == [
            "e203_controller_manual.md#chunk-0",
            "e404_sensor_manual.md#chunk-0",
        ]
        assert device_risks["DEV-002"]["knowledge_sources"] == [
            "e404_sensor_manual.md#chunk-0"
        ]
        assert device_risks["DEV-003"]["knowledge_sources"] == [
            "e101_maintenance_manual.md#chunk-0"
        ]
        assert device_risks["DEV-005"]["knowledge_sources"] == [
            "e201_vibration_manual.md#chunk-0"
        ]
        assert device_risks["DEV-004"]["risk_level"] == "normal"

        assert any("E101" in query and "handling steps" in query for query in knowledge_queries)
        assert any("E201" in query and "handling steps" in query for query in knowledge_queries)
        assert any("E203" in query and "handling steps" in query for query in knowledge_queries)
        assert sum(1 for query in knowledge_queries if "E404" in query) == 2

        llm_user_payload = json.loads(provider.calls[0][1].content)
        assert "knowledge_evidence" in llm_user_payload
        assert [
            (item["device_code"], item["alarm_code"], item["results"][0]["source"])
            for item in llm_user_payload["knowledge_evidence"]
        ] == [
            ("DEV-001", "E203", "e203_controller_manual.md#chunk-0"),
            ("DEV-001", "E404", "e404_sensor_manual.md#chunk-0"),
            ("DEV-002", "E404", "e404_sensor_manual.md#chunk-0"),
            ("DEV-003", "E101", "e101_maintenance_manual.md#chunk-0"),
            ("DEV-005", "E201", "e201_vibration_manual.md#chunk-0"),
        ]

        assert history_response.status_code == 200
        history = history_response.json()
        assert len(history) == 1
        assert history[0]["problem_summary"] == "Fleet risk report generated by LLM."
        assert history[0]["sources"] == data["sources"]

        detail_response = client.get(f"/diagnosis/history/{history[0]['report_id']}")
        assert detail_response.status_code == 200
        detail = detail_response.json()
        assert detail["response"] is None
        assert detail["risk_report"]["summary"] == "Fleet risk report generated by LLM."
        assert detail["rag_sources"][0]["source"] == "e203_controller_manual.md#chunk-0"

        trace = detail["trace"]
        assert trace["mode"] == "multi_device_risk"
        assert trace["router_tools"] == [
            "list_devices",
            "get_device_status",
            "get_device_alarms",
            "search_knowledge",
        ]

        tool_results = trace["tool_results"]
        status_calls = [
            item for item in tool_results if item["tool_name"] == "get_device_status"
        ]
        alarm_calls = [
            item for item in tool_results if item["tool_name"] == "get_device_alarms"
        ]
        knowledge_calls = [
            item for item in tool_results if item["tool_name"] == "search_knowledge"
        ]
        assert [item["result"]["device_code"] for item in status_calls] == [
            "DEV-001",
            "DEV-002",
            "DEV-003",
            "DEV-004",
            "DEV-005",
        ]
        assert [item["result"]["device_code"] for item in alarm_calls] == [
            "DEV-001",
            "DEV-002",
            "DEV-003",
            "DEV-004",
            "DEV-005",
        ]
        assert [
            (item["result"]["device_code"], item["result"]["alarm_code"])
            for item in knowledge_calls
        ] == [
            ("DEV-001", "E203"),
            ("DEV-001", "E404"),
            ("DEV-002", "E404"),
            ("DEV-003", "E101"),
            ("DEV-005", "E201"),
        ]
        assert [
            (item["device_code"], item["alarm_code"], item["query"], item["source"])
            for item in trace["rag_results"]
        ] == [
            ("DEV-001", "E203", "E203 电机运行异常 handling steps", "e203_controller_manual.md#chunk-0"),
            ("DEV-001", "E404", "E404 通信异常 handling steps", "e404_sensor_manual.md#chunk-0"),
            ("DEV-002", "E404", "E404 通信异常 handling steps", "e404_sensor_manual.md#chunk-0"),
            ("DEV-003", "E101", "E101 温度异常 handling steps", "e101_maintenance_manual.md#chunk-0"),
            ("DEV-005", "E201", "E201 振动异常 handling steps", "e201_vibration_manual.md#chunk-0"),
        ]
    finally:
        app.dependency_overrides.clear()
        Base.metadata.drop_all(bind=SessionLocal.kw["bind"])


