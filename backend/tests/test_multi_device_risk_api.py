def _knowledge_result(
    *,
    alarm_code: str,
    source: str,
    filename: str,
    content: str,
    distance: float,
) -> KnowledgeSearchToolResult:
    return KnowledgeSearchToolResult(
        ok=True,
        results=[
            ToolKnowledgeResult(
                chunk_id={"E101": 1, "E201": 4, "E203": 2, "E404": 3}[alarm_code],
                document_id={"E101": 1, "E201": 4, "E203": 2, "E404": 3}[alarm_code],
                filename=filename,
                chunk_index=0,
                content=content,
                source=source,
                distance=distance,
            )
        ],
    )
